"""
PDF Generator Agent — port 8009

Renders a curated day-by-day itinerary (produced by the Trip Planner agent)
into a polished PDF travel guide. Layout:

  - Cover page          : video source, thumbnail, trip date, total cost
  - One page per day    : theme, day cost, each stop with activity, duration,
                          address, GPS, and nearby restaurants
  - Final maps page     : full Google Maps road-trip link
"""

